function CarouselController()
{
	var node = document.createElement( "div" );
		node.setAttribute( "id", "carousel" );
		node.setAttribute( "class", "carousel slide" );
		node.setAttribute( "data-ride", "carousel" );
		
	var inner = document.createElement( "div" );
		inner.setAttribute( "class", "carousel-inner" );
		node.appendChild( inner );
	
	
	var left_control = document.createElement( "a" );
		left_control.setAttribute( "class", "left carousel-control" );
		left_control.setAttribute( "href", "#carousel" );
		left_control.setAttribute( "role", "button" );
		left_control.setAttribute( "data-slide", "prev" );
		left_control.innerHTML = "<span class='glyphicon glyphicon-chevron-left'></span>";
		node.appendChild( left_control );
	
	var right_control = document.createElement( "a" );
		right_control.setAttribute( "class", "right carousel-control" );
		right_control.setAttribute( "href", "#carousel" );
		right_control.setAttribute( "role", "button" );
		right_control.setAttribute( "data-slide", "next" );
		right_control.innerHTML = "<span class='glyphicon glyphicon-chevron-right'></span>";
		node.appendChild( right_control );
	
	this.node = node;
		this.node.controller = this;
	this.inner = inner;
	this.slides = [];
}

CarouselController.prototype.addSlide = function( image_url )
{
	var slide = document.createElement( "div" );
		slide.setAttribute( "class", (this.slides.length == 0) ? "item active" : "item" );
		var img = document.createElement( "img" );
			img.setAttribute( "src", image_url );
			slide.appendChild( img );
		var caption = document.createElement( "div" );
			caption.setAttribute( "class", "carousel-caption" );
			caption.innerText = "Caption?";
			slide.appendChild( caption );
	this.inner.appendChild( slide );
	this.slides.push( slide );
}

CarouselController.prototype.clearSlides = function()
{
	for ( i=0, i_end=this.slides.length; i<i_end; ++i )
	{
		this.inner.removeChild( this.slides[i] );
	}
	this.slides = [];
}

function LinkBarController()
{
	var node = document.createElement( "div" );
		node.setAttribute( "class", "navbar navbar-default games-linkbar" );
	this.node = node;
	this.link_nodes = [];
}

LinkBarController.prototype.addLinkButton = function( buttoninfo )
{
	var link_node = document.createElement( "a" );
		// link_node.target = "_blank";
		link_node.href = buttoninfo.url;
		var button_node = document.createElement( "div" );
			var button_class = "btn btn-default btn-xs game-linkbutton";
			if ( buttoninfo.additional_classes != undefined )
			{
				button_class += " "+buttoninfo.additional_classes;
			}
			button_node.setAttribute( "class", button_class );
			
			if ( buttoninfo.icon != undefined )
			{
				var icon_node = document.createElement( "img" );
					icon_node.setAttribute( "src", buttoninfo.icon );
					button_node.appendChild( icon_node );
			}
				
			var text_node = document.createElement( "div" );
				text_node.setAttribute( "class", "game-linkbutton-text" );
				text_node.innerHTML = buttoninfo.text;
				button_node.appendChild( text_node );
				
			link_node.appendChild( button_node );
		this.node.appendChild( link_node );
	this.link_nodes.push( link_node );
}

LinkBarController.prototype.clearLinks = function()
{
	for ( i=0, i_end=this.link_nodes.length; i<i_end; ++i )
	{
		this.node.removeChild( this.link_nodes[i] );
	}
	this.link_nodes = [];
}

function LinkButtonInfo( text, url )
{
	this.icon = "undefined"
	this.text = (text==undefined)?"undefined":text;
	this.url = (url==undefined)?"undefined":url;
}

function GameInfo()
{
	this.title = "Empty Game Title";
	this.description = "Empty Game Description";
	this.gallery_images = [];
	this.links = {};
}

function GamePageController()
{
	var node = document.createElement( "div" );
		node.setAttribute( "class", "panel panel-default gamesheet" );
		
		var header = document.createElement( "div" );
			header.setAttribute( "class", "panel-heading gamesheet-header" );
			node.appendChild( header );
			var title_node = document.createElement( "h1" );
				title_node.innerText = "#Game Title";
				header.appendChild( title_node );
		
		var linkBarController = new LinkBarController();
			node.appendChild( linkBarController.node );
		
		var contentNode = document.createElement( "div" );
			contentNode.setAttribute( "class", "container full-width" );
			contentNode.setAttribute( "style", "height:auto;" );
			node.appendChild( contentNode );
		
			var carouselContainerNode = document.createElement( "div" );
				carouselContainerNode.setAttribute( "class", "games-carousel-container" );
				contentNode.appendChild( carouselContainerNode );
				var carouselController = new CarouselController();
					carouselContainerNode.appendChild( carouselController.node );
				
			var descriptionNode = document.createElement( "div" );
				contentNode.appendChild( descriptionNode );
			
		
	this.node = node;
	this.carouselController = carouselController;
	this.title_node = title_node;
	this.linkBarController = linkBarController;
	this.descriptionNode = descriptionNode;
	
}


GamePageController.prototype.setGame = function( gameinfo )
{
	this.title_node.innerText = gameinfo.title;
	this.carouselController.clearSlides();
	this.linkBarController.clearLinks();
	
	for ( i=0, i_end=gameinfo.gallery_images.length; i<i_end; ++i )
	{
		this.carouselController.addSlide( gameinfo.gallery_images[i] );
	}
	
	for ( i=0, i_end=gameinfo.links.length; i<i_end; ++i )
	{
		this.linkBarController.addLinkButton( gameinfo.links[i] );
	}
	this.descriptionNode.innerHTML = gameinfo.description;
}



































